from django.db import models
from .usuario import Usuario

class Paciente (models.Model):
    idPaciente = models.AutoField(primary_key=True)
    usuario = models.ForeignKey(Usuario, related_name='paciente', on_delete=models.CASCADE, null=True)
    fechaIngreso = models.DateField(null=True)
    fechaSalida = models.DateField(null=True)
    isActivo = models.BooleanField(default=True)
    diagnostico = models.CharField ('diagnostico', max_length=250, null=True)
    






























































































































































































































































































































































































































